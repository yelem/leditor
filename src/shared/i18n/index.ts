const DICTS: Record<UiLanguage, Record<TranslationKey, string>> = { en, uk, ru }

/** Название языка для переключателя (всегда на самом языке). */
export const LANGUAGE_LABELS: Record<UiLanguage, string> = {
  en: 'English',
  uk: 'Українська',
  ru: 'Русский'
}

/** Локаль для форматирования дат/чисел. */
export const LANGUAGE_LOCALES: Record<UiLanguage, string> = {
  en: 'en-US',
  uk: 'uk-UA',
  ru: 'ru-RU'
}

export function translate(
  lang: UiLanguage,
  key: TranslationKey,
  vars?: Record<string, string | number>
): string {
  let s: string = DICTS[lang]?.[key] ?? en[key] ?? key
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      s = s.replace(`{${name}}`, String(value))
    }
  }
  return s
}

export type Translator = (key: TranslationKey, vars?: Record<string, string | number>) => string

export function makeTranslator(lang: UiLanguage): Translator {
  return (key, vars) => translate(lang, key, vars)
}
